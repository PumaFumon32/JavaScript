let a = 10; 

// Relacionales
console.log (a > 5); // Preguntar si una variable o cosa es mayor a 5
console.log (a >= 5); // Preguntar si una variable o cosa es mayor o igual a 5
console.log (a < 5); // Preguntar si una variable o cosa es menor a 5
console.log (a >= 5); // Preguntar si una variable o cosa es menor o igual a 5

// Igualdad
console.log(a == 10); // Comprueba si la variable vale 10
console.log(a != 10); // Comprueba si son diferentes o no 
console.log(a == "10"); // El igual solo comprueba si el valor es igual, da igual el tipo de dato que pongas
console.log(a === "10"); // Para comprobar si es el mismo tipo de valor y tipo de dato se utiliza el triple igual

