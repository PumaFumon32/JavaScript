// Hay 3: And, or, Not

// AND se pone como &&
console.log(true && true); // Solo sale true si ambos son iguales
let mayor = true;
let suscrito = true;
console.log(mayor && suscrito);

// Or se utiliza ||
console.log(true || true); // Sale true si uno de los dos es true, si ambos son false saldra false

// Not se usa con !
console.log(!true); // Lo puesto se niega
